
public class WtoFor {

	public static void main(String[] args) {
	
		for(int limit=10,x=1;x<=limit;x++) {
			for(int n=x,i=0;i<n;i++) {
				System.out.print("*");
			}
			System.out.println("");
			}
		for(int x=9;x>=1;x--) {
			for(int n=x,i=0;i<n;i++) {
				System.out.print("*");
			}
			System.out.println("");
			}
		}
}
